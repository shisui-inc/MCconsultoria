document.addEventListener("DOMContentLoaded", () => {
    // 1. Collect all text nodes and store original text
    const walk = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
    let node;
    const textNodes = [];
    while (node = walk.nextNode()) {
        if (!node.parentElement || node.parentElement.tagName === 'SCRIPT' || node.parentElement.tagName === 'STYLE') continue;
        const text = node.nodeValue.trim();
        // Exact matching
        if (text && dictionary[text] !== undefined) {
            textNodes.push({ node, original: text });
        }
    }

    // 2. Map form placeholders
    const inputs = document.querySelectorAll('input[placeholder], textarea[placeholder]');
    inputs.forEach(el => {
        el.setAttribute('data-original-placeholder', el.getAttribute('placeholder'));
    });

    // 3. Attach click events
    const btns = document.querySelectorAll('.lang-btn');
    if (!btns.length) return;

    btns.forEach(btn => {
        btn.addEventListener('click', () => {
            btns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            const lang = btn.getAttribute('data-lang');

            // Text nodes
            textNodes.forEach(item => {
                if (lang === 'es') {
                    item.node.nodeValue = item.original;
                } else {
                    item.node.nodeValue = dictionary[item.original][lang] || item.original;
                }
            });

            // Placeholders
            inputs.forEach(el => {
                const orig = el.getAttribute('data-original-placeholder');
                // Basic matching for placeholders
                if (orig && orig.includes('Juan')) {
                    el.placeholder = lang === 'es' ? 'Ej: Juan García' : (lang === 'en' ? 'Ex: John Doe' : 'Ex: João Silva');
                } else if (orig && orig.includes('email')) {
                    el.placeholder = lang === 'es' ? 'tu@email.com' : 'you@email.com';
                } else if (orig && orig.includes('Argentino')) {
                    el.placeholder = lang === 'es' ? 'Ej: Brasileño, Argentino...' : (lang === 'en' ? 'Ex: Brazilian, American...' : 'Ex: Brasileiro, Argentino...');
                } else if (orig && orig.includes('consult')) {
                    el.placeholder = lang === 'es' ? 'Describí tu consulta...' : (lang === 'en' ? 'Describe your needs...' : 'Descreva sua consulta...');
                }
            });
        });
    });
});
